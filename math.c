#include<stdio.h>
#include<math.h>
void main()
{
	printf("\n ceil function    : %.2f",ceil(9.3));
	printf("\n ceil function    : %.2f",ceil(9.9));
	
	printf("\n floor function   : %.2f",floor(9.3));
	printf("\n floor function   : %.2f",floor(9.9));
	
	printf("\n sqrt function    : %.2f",sqrt(16));
	printf("\n sqrt function    : %.2f",sqrt(11));
	
	printf("\n power function   : %.2f",pow(4,2));
	printf("\n power function   : %.2f",pow(2.1,2.2));
	
	printf("\n abs function     : %d",abs(-22));
	getch();
}
